    'function [u_s, i_s, Pout, Pin, eta, dPin_dt, current_saturated] = Power_Efficiency_Monitor(u_d, u_q, id, iq, omega_m, Te, Pin_prev)\n' ...
    '%%#codegen\n' ...
    'Ts=%.17g; Imax=%.17g;\n' ...
    'u_s = hypot(u_d, u_q);\n' ...
    'i_s = hypot(id, iq);\n' ...
    'Pin = 1.5 * (u_d * id + u_q * iq);\n' ...
    'Pout = max(0, Te * omega_m);\n' ...
    'eta = 0;\n' ...
    'if Pin > 1e-9\n' ...
    '    eta = min(max(Pout / Pin, 0), 1.2);\n' ...
    'end\n' ...
    'dPin_dt = (Pin - Pin_prev) / max(Ts, eps);\n' ...
    'current_saturated = double(i_s > Imax * 0.999);\n' ...
    'end\n'], p.Ts, p.Imax);
end
