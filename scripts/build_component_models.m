function build_component_model(modelPath, method, p)
[~, modelName] = fileparts(modelPath);
pModel = method_tuned_parameters(p, method);
if bdIsLoaded(modelName)
    close_system(modelName, 0);
end
if exist(modelPath, 'file') == 2
    delete(modelPath);
end

new_system(modelName);
open_system(modelName);
set_param(modelName, ...
    'StopTime', num2str(p.Tstop), ...
    'SolverType', 'Fixed-step', ...
    'Solver', 'FixedStepDiscrete', ...
    'FixedStep', num2str(p.Ts), ...
    'SaveFormat', 'StructureWithTime', ...
    'ReturnWorkspaceOutputs', 'on', ...
    'SignalLogging', 'off');

add_io_blocks(modelName);
add_state_blocks(modelName, pModel);
add_component_blocks(modelName, method, pModel);
add_output_blocks(modelName);
wire_component_model(modelName);
add_component_annotations(modelName, method, pModel);

save_system(modelName, modelPath);
set_param(modelName, 'Dirty', 'off');
close_system(modelName, 0);
end

function pModel = method_tuned_parameters(p, method)
pModel = p;
if ~isfield(pModel.optimizer, 'search_bias_rate')
    pModel.optimizer.search_bias_rate = 0;
end
if ~isfield(pModel.optimizer, 'search_bias_tau')
    pModel.optimizer.search_bias_tau = 1;
end
if ~isfield(pModel.optimizer, 'search_bias_stop_id')
    pModel.optimizer.search_bias_stop_id = -Inf;
end
if isfield(p, 'speed_by_method') && isfield(p.speed_by_method, method)
    s = p.speed_by_method.(method);
    if isfield(s, 'Kp'), pModel.speed.Kp = s.Kp; end
    if isfield(s, 'Ki'), pModel.speed.Ki = s.Ki; end
end
if strcmp(method, 'optimization') && isfield(p, 'optimizer_for_component')
    f = fieldnames(p.optimizer_for_component);
    for k = 1:numel(f)
        pModel.optimizer.(f{k}) = p.optimizer_for_component.(f{k});
    end
end
end

