function txt = current_controller_script(p)
txt = sprintf([ ...
    'function [u_d, u_q, id_int_next, iq_int_next, voltage_saturated] = Current_Controller_PI(id_ref, iq_ref, id, iq, omega_m, id_int, iq_int)\n' ...
    '%%#codegen\n' ...
    'Ts=%.17g; polePairs=%.17g; Ld=%.17g; Lq=%.17g; psi=%.17g; Vmax=%.17g;\n' ...
    'Kpd=%.17g; Kid=%.17g; Kpq=%.17g; Kiq=%.17g; intLimit=%.17g;\n' ...
    'omega_e = polePairs * omega_m;\n' ...
    'e_d = id_ref - id;\n' ...
    'e_q = iq_ref - iq;\n' ...
    'id_int_try = id_int + e_d * Ts;\n' ...
    'iq_int_try = iq_int + e_q * Ts;\n' ...
    'u_d_raw = Kpd * e_d + Kid * id_int_try - omega_e * Lq * iq;\n' ...
    'u_q_raw = Kpq * e_q + Kiq * iq_int_try + omega_e * (Ld * id + psi);\n' ...
    'u_raw = hypot(u_d_raw, u_q_raw);\n' ...
    'voltage_saturated = double(u_raw > Vmax);\n' ...
    'if voltage_saturated > 0.5\n' ...
    '    scale = Vmax / max(u_raw, eps);\n' ...
    '    u_d = u_d_raw * scale;\n' ...
    '    u_q = u_q_raw * scale;\n' ...
    '    id_int_next = id_int;\n' ...
    '    iq_int_next = iq_int;\n' ...
    'else\n' ...
    '    u_d = u_d_raw;\n' ...
    '    u_q = u_q_raw;\n' ...
    '    id_int_next = clamp(id_int_try, -intLimit, intLimit);\n' ...
    '    iq_int_next = clamp(iq_int_try, -intLimit, intLimit);\n' ...
    'end\n' ...
    'end\n\n' ...
    'function y = clamp(x, lo, hi)\n' ...
    'y = min(max(x, lo), hi);\n' ...
    'end\n'], p.Ts, p.pole_pairs, p.Ld, p.Lq, p.psi_f, p.Vmax, ...
