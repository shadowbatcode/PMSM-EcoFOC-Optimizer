end

function txt = plant_script(p)
txt = sprintf([ ...
    'function [id_next, iq_next, omega_next, omega_rpm, did_dt, Te] = PMSM_dq_Plant(u_d, u_q, id, iq, omega_m, T_L)\n' ...
    '%%#codegen\n' ...
    'Ts=%.17g; polePairs=%.17g; Rs=%.17g; Ld=%.17g; Lq=%.17g; psi=%.17g; J=%.17g; B=%.17g;\n' ...
    'omega_e = polePairs * omega_m;\n' ...
    'did_dt = (u_d - Rs * id + omega_e * Lq * iq) / Ld;\n' ...
    'diq_dt = (u_q - Rs * iq - omega_e * (Ld * id + psi)) / Lq;\n' ...
    'Te = 1.5 * polePairs * (psi * iq + (Ld - Lq) * id * iq);\n' ...
    'domega_dt = (Te - T_L - B * omega_m) / J;\n' ...
    'id_next = id + Ts * did_dt;\n' ...
    'iq_next = iq + Ts * diq_dt;\n' ...
    'omega_next = max(0, omega_m + Ts * domega_dt);\n' ...
    'omega_rpm = omega_next * 60 / (2*pi);\n' ...
    'end\n'], p.Ts, p.pole_pairs, p.Rs, p.Ld, p.Lq, p.psi_f, p.J, p.B);
end

function txt = power_monitor_script(p)
