function txt = speed_controller_script(p)
txt = sprintf([ ...
    'function [iq_ref, speed_int_next, speed_error] = Speed_Controller_PID(omega_ref, omega_m, speed_int)\n' ...
    '%%#codegen\n' ...
    'Ts = %.17g; Kp = %.17g; Ki = %.17g; Imax = %.17g; intLimit = %.17g;\n' ...
    'speed_error = omega_ref - omega_m;\n' ...
    'speed_int_next = clamp(speed_int + speed_error * Ts, -intLimit, intLimit);\n' ...
    'iq_raw = Kp * speed_error + Ki * speed_int_next;\n' ...
    'iq_ref = clamp(iq_raw, -Imax, Imax);\n' ...
    'if abs(iq_ref - iq_raw) > 1e-12 && sign(speed_error) == sign(iq_raw)\n' ...
    '    speed_int_next = speed_int;\n' ...
    'end\n' ...
    'end\n\n' ...
    'function y = clamp(x, lo, hi)\n' ...
    'y = min(max(x, lo), hi);\n' ...
    'end\n'], p.Ts, p.speed.Kp, p.speed.Ki, p.Imax, p.speed_int_limit);
end

function txt = optimizer_script(p)
txt = sprintf([ ...
    'function [id_ref_raw, Pin_lpf_next, g_hat_lpf_next, id_bar_next, id_bar_before, steady_count_next, perturb_ramp_next, P_ac, demod_signal, g_hat, steady_flag, optimizer_enable, freeze_state, projection_active, perturbation] = ModelFree_Optimizer(method_flag, t, speed_error, id, iq, iq_ref, Pin_state, Pin_prev_state, Pin_lpf, g_hat_lpf, id_bar, u_s_prev, id_ref_prev, steady_count, perturb_ramp, current_saturated_prev, voltage_saturated_prev)\n' ...
    '%%#codegen\n' ...
    'Ts=%.17g; omegaErrTh=%.17g; idErrTh=%.17g; iqErrTh=%.17g; powerSlopeTh=%.17g; holdTime=%.17g;\n' ...
    'amp=%.17g; omegaD=%.17g; pinTau=%.17g; gradTau=%.17g; alpha=%.17g; updatePeriod=%.17g; maxStep=%.17g; rateLimit=%.17g; resumeRamp=%.17g; freezeRamp=%.17g;\n' ...
    'searchBiasRate=%.17g; searchBiasTau=%.17g; searchBiasStopId=%.17g;\n' ...
    'Imax=%.17g; idMin=%.17g; idMax=%.17g; Vmax=%.17g; vProjStep=%.17g;\n' ...
    'id_bar_before = id_bar;\n' ...
    'P_ac = Pin_state - Pin_lpf;\n' ...
    'demod_signal = 0;\n' ...
    'g_hat = g_hat_lpf;\n' ...
    'steady_flag = 0;\n' ...
    'optimizer_enable = 0;\n' ...
    'projection_active = 0;\n' ...
    'freeze_state = 1;\n' ...
    'perturbation = 0;\n' ...
    'Pin_lpf_next = Pin_lpf;\n' ...
    'g_hat_lpf_next = g_hat_lpf;\n' ...
    'id_bar_next = id_bar;\n' ...
    'steady_count_next = steady_count;\n' ...
    'perturb_ramp_next = perturb_ramp;\n' ...
    'if method_flag > 0.5\n' ...
    '    dPin_dt = (Pin_state - Pin_prev_state) / max(Ts, eps);\n' ...
    '    cond_speed = abs(speed_error) < omegaErrTh;\n' ...
    '    cond_id = abs(id_ref_prev - id) < idErrTh;\n' ...
    '    cond_iq = abs(iq_ref - iq) < iqErrTh;\n' ...
    '    cond_power = abs(dPin_dt) < powerSlopeTh;\n' ...
    '    cond_sat = ~(current_saturated_prev > 0.5 || voltage_saturated_prev > 0.5);\n' ...
    '    if cond_speed && cond_id && cond_iq && cond_power && cond_sat\n' ...
    '        steady_count_next = steady_count + 1;\n' ...
    '    else\n' ...
    '        steady_count_next = 0;\n' ...
    '    end\n' ...
    '    steady_flag = double(steady_count_next * Ts >= holdTime);\n' ...
    '    beta_pin = Ts / (pinTau + Ts);\n' ...
    '    beta_grad = Ts / (gradTau + Ts);\n' ...
    '    Pin_lpf_next = Pin_lpf + beta_pin * (Pin_state - Pin_lpf);\n' ...
    '    P_ac = Pin_state - Pin_lpf_next;\n' ...
    '    sin_d = sin(omegaD * t);\n' ...
    '    demod_signal = P_ac * sin_d;\n' ...
    '    g_hat_lpf_next = g_hat_lpf + beta_grad * (demod_signal - g_hat_lpf);\n' ...
    '    g_hat = 2 * g_hat_lpf_next / max(amp, eps);\n' ...
    '    if steady_flag > 0.5\n' ...
    '        optimizer_enable = 1;\n' ...
    '        freeze_state = 0;\n' ...
    '        update = clamp(-alpha * g_hat * updatePeriod, -maxStep, maxStep);\n' ...
    '        if searchBiasRate > 0 && id_bar > searchBiasStopId\n' ...
    '            biasAge = max(t - holdTime, 0);\n' ...
    '            biasRamp = 1 - exp(-biasAge / max(searchBiasTau, Ts));\n' ...
    '            update = update + searchBiasRate * biasRamp * (searchBiasStopId - id_bar) * Ts;\n' ...
    '        end\n' ...
    '        id_bar_candidate = id_bar + update;\n' ...
    '        [id_bar_projected, projection_active] = project_id_reference(id_bar_candidate, iq_ref, u_s_prev, Imax, idMin, idMax, Vmax, vProjStep);\n' ...
    '        id_bar_next = slew_limit(id_bar_projected, id_bar, rateLimit, Ts);\n' ...
    '        perturb_ramp_next = min(1, perturb_ramp + Ts / max(resumeRamp, Ts));\n' ...
    '        perturbation = perturb_ramp_next * amp * sin_d;\n' ...
    '    else\n' ...
    '        perturb_ramp_next = max(0, perturb_ramp - Ts / max(freezeRamp, Ts));\n' ...
    '        perturbation = perturb_ramp_next * amp * sin(omegaD * t);\n' ...
    '    end\n' ...
    '    id_ref_raw = id_bar_next + perturbation;\n' ...
    'else\n' ...
    '    id_ref_raw = 0;\n' ...
    'end\n' ...
    'end\n\n' ...
    'function [id_projected, active] = project_id_reference(id_candidate, iq_ref, u_s_est, Imax, idMin, idMax, Vmax, vProjStep)\n' ...
    'id_projected = clamp(id_candidate, idMin, idMax);\n' ...
    'box_active = abs(id_projected - id_candidate) > 1e-12;\n' ...
    'id_limit = sqrt(max(Imax^2 - iq_ref^2, 0));\n' ...
    'id_after_current = clamp(id_projected, -id_limit, id_limit);\n' ...
    'current_active = abs(id_after_current - id_projected) > 1e-12;\n' ...
    'id_projected = clamp(id_after_current, idMin, idMax);\n' ...
    'voltage_active = u_s_est > Vmax * 0.995;\n' ...
    'if voltage_active\n' ...
    '    id_projected = max(idMin, id_projected - vProjStep);\n' ...
    'end\n' ...
    'id_projected = clamp(id_projected, idMin, idMax);\n' ...
    'active = double(box_active || current_active || voltage_active);\n' ...
    'end\n\n' ...
    'function y = slew_limit(target, current, rate, Ts)\n' ...
    'delta = clamp(target - current, -rate * Ts, rate * Ts);\n' ...
    'y = current + delta;\n' ...
    'end\n\n' ...
    'function y = clamp(x, lo, hi)\n' ...
    'y = min(max(x, lo), hi);\n' ...
    'end\n'], ...
    p.Ts, p.steady.omega_error_threshold, p.steady.id_error_threshold, ...
    p.steady.iq_error_threshold, p.steady.power_slope_threshold, p.steady.hold_time, ...
    p.optimizer.perturbation_amplitude, p.optimizer.omega_d, p.optimizer.power_lpf_tau, ...
    p.optimizer.gradient_lpf_tau, p.optimizer.alpha, p.optimizer.update_period, ...
    p.optimizer.max_id_bar_step, p.optimizer.id_rate_limit, p.optimizer.resume_ramp_time, ...
    p.optimizer.freeze_ramp_time, p.optimizer.search_bias_rate, p.optimizer.search_bias_tau, ...
    p.optimizer.search_bias_stop_id, p.Imax, p.id_min, p.id_max, p.Vmax, ...
    p.optimizer.voltage_projection_step);
end

